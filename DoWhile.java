import java.util.ArrayList;
//Importamos a classe ArrayList para pode fazer uso do vetor e armazenar elementos
public class DoWhile {
    /* Método principal da classe */
    public static void main (String[] args) { //Fluxo de repetição usando Do While
        int i = 3; // a variavel i recebe o valor cinco (5)
        do {
            System.out.println(i); //imprima valor de i 
            i++; //incremento da variavel i
        }
        while (i < 5); { //enquanto i for menor que 3
        }//fim do método
    }//fim do método
}//fim da classe