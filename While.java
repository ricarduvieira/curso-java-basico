import java.util.ArrayList;
//Importamos a classe ArrayList para poder fazer uso do vetor e armazenar elementos

public class While {
    /* Método principal da classe */
    public static void main (String[] args) {
        //Fluxo de repetição usando o Whille
        int i = 0; //a variavel i recebe o valor zero (0)
        while (i < 2) { // enquanto i for menor que 2
            System.out.println (i); //imprima valor de i
            i ++; //incremento da variavel i
        }//fim do método
    }//fim do método
}//fim da classe